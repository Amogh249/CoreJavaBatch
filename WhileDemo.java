public class WhileDemo
{
	public static void main(String[] args)
	{
		int i=0;
		while(i<10)
		{
			//print function will display the statement
			//and put cursor on same line
			//whereas println function will display the 
			//statement and put cursor on next line
			System.out.println("Hello");
			i++;
		}
	}
}